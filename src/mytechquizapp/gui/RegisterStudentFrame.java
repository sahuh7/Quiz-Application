    private int validateInputs() {
        userId = txtUserId.getText();
        password = String.valueOf(txtPassword.getPassword());
        rtPassword = String.valueOf(txtReTypePassword.getPassword());
        if(userId.isEmpty() || password.isEmpty() || rtPassword.isEmpty())
            return 0;
        else if(userId.length()<4 || password.length()<4)
            return -1;
        else if(!password.equals(rtPassword))            
            return -2;        
        else
            return 1;
    }
    
    private void clearAll(){
        txtPassword.setText("");
        txtUserId.setText("");
        txtReTypePassword.setText("");
   }
