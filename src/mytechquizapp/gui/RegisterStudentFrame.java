    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int ans = validateInputs();
        if(ans==0){
            JOptionPane.showMessageDialog(null, "Please fill all the fields!", "Missing Fields!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(ans==-1){
            JOptionPane.showMessageDialog(null, "User ID or Password should be atleast 4 characters!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(ans==-2){
            JOptionPane.showMessageDialog(null, "Password and Re-Type Password not match!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            Users user = new Users();
            user.setUserID(userId);
            user.setPassword(password);
            user.setUserType("student");
            boolean status = UsersDAO.addUser(user);
            if(status){
                JOptionPane.showMessageDialog(null, "Student Registered Successfully!!", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }
            else{
                JOptionPane.showMessageDialog(null, "User ID already exist!!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "SQL Error : "+ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

