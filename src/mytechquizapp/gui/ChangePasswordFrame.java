    private void btnUpdatePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePasswordActionPerformed
        int ans = validateInputs();
        if(ans==0){
            JOptionPane.showMessageDialog(null, "Please fill all the fields!", "Missing Fields!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(ans==-1){
            JOptionPane.showMessageDialog(null, "New Password should be atleast 4 characters!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(ans==-2){
            JOptionPane.showMessageDialog(null, "Old Password and New Password should be different!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(ans==-3){
            JOptionPane.showMessageDialog(null, "New Password and Re-Type New Password not match!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            Users user = new Users();
            user.setUserID(UserProfile.getUsername());
            user.setPassword(oldPassword);
            user.setUserType(UserProfile.getUsertype());
            boolean status = UsersDAO.verifyCredentials(user);
            if(status){
                boolean response = UsersDAO.updatePassword(UserProfile.getUsername(), newPassword);
                if(response){
                    JOptionPane.showMessageDialog(null, "Password Changed Successfully!!", "Success", JOptionPane.INFORMATION_MESSAGE);                
                    btnBackActionPerformed(evt);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Password not updated!!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Old Password not matched!!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "SQL Error : "+ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdatePasswordActionPerformed

