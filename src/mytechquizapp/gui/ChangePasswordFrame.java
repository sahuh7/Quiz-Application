    private int validateInputs() {
        oldPassword = String.valueOf(txtOldPassword.getPassword());
        newPassword = String.valueOf(txtNewPassword.getPassword());
        rtNewPassword = String.valueOf(txtReTypeNewPassword.getPassword());
        if(oldPassword.isEmpty() || newPassword.isEmpty() || rtNewPassword.isEmpty())
            return 0;
        else if(newPassword.length()<4)
            return -1;
        else if(oldPassword.equals(newPassword))
            return -2;
        else if(!newPassword.equals(rtNewPassword))            
            return -3;        
        else
            return 1;
    }
